#include <stdio.h>

/**
 * add - add two integers
 * @a: firts int
 * @b: second int
 * Return addition
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * mod - mod two integers
 * @a: firts int
 * @b: second int
 * Return final result
 */

int mod(int a, int b)
{
       return (a % b);                  
}       

/**
 * mul - multiply of two integers
 * @a: firts int
 * @b: second int
 * Return  final result
 */

int mul(int a, int b)
{
        return (a * b);
}

/**
 * sub - subtract two integers
 * @a: firts int
 * @b: second int
 * Return subtracted final result
 */

int sub(int a, int b)
{
        return (a - b);
}

/**
 * div - division of two integers
 * @a: firts int
 * @b: second int
 * Return  final result
 */

int div(int a, int b)
{
        return (a / b);
}                             
